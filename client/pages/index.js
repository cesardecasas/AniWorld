import Head from 'next/head'
import { useEffect, useState } from 'react'
import AnimeCard from '../components/AnimeCard'
import styles from '../styles/Home.module.css'
import {getUpcoming} from './api/fetch'

export default function Home() {
  const [animes, setAnimes] = useState([])

  const populate = async()=>{
    const res = await getUpcoming()
    setAnimes(res.top)
    console.log(animes) 
  }

  useEffect(()=>{
    populate()
  },[])

  return (
    <div className={styles.container}>
      <Head>
        <title>Create Next App</title>
        <meta name="description" content="Generated by create next app" />
        <link rel="icon" href="/favicon.ico" />
      </Head>
      <h1> New Realeases</h1>
      <section style={{display:'grid', gridTemplateColumns:'1fr 1fr 1fr 1fr', width:'80%', marginLeft:'3%', maxHeight:'1vh'}}>
        {animes[0] ? animes.map((anime, i)=> <AnimeCard key={i} name={anime.title} image={anime.image_url} date={anime.start_date} id={anime.mal_id}/>)  : <p>Loading</p>}

      </section>

      
    </div>
  )
}
